void draw_set_color(SDL_Renderer *r, SDL_Color c) 
{
    SDL_SetRenderDrawColor(r, c.r, c.g, c.b, c.a);
}

void draw_fill_rect(SDL_Renderer *r, int x, int y, int w, int h) 
{
    SDL_Rect rc = {x, y, w, h};
    SDL_RenderFillRect(r, &rc);
}

void draw_outline_rect(SDL_Renderer *r, int x, int y, int w, int h) 
{
    SDL_Rect rc = {x, y, w, h};
    SDL_RenderDrawRect(r, &rc);
}

void draw_overlay(SDL_Renderer *r, uint8_t alpha) 
{
    SDL_SetRenderDrawBlendMode(r, SDL_BLENDMODE_BLEND);
    SDL_SetRenderDrawColor(r, 0x00, 0x00, 0x00, alpha);
    draw_fill_rect(r, 0, 0, SCR_WIDTH, SCR_HEIGHT);
    SDL_SetRenderDrawBlendMode(r, SDL_BLENDMODE_NONE);
}

void draw_color_overlay(SDL_Renderer *r, uint8_t rr, uint8_t gg, uint8_t bb) 
{
    SDL_SetRenderDrawBlendMode(r, SDL_BLENDMODE_BLEND);
    SDL_SetRenderDrawColor(r, rr, gg, bb, 0x90);
    draw_fill_rect(r, 0, 0, SCR_WIDTH, SCR_HEIGHT);
    SDL_SetRenderDrawBlendMode(r, SDL_BLENDMODE_NONE);
}

static void draw_char(SDL_Renderer *r, char ch, int x, int y, int scale) 
{
    int idx = (unsigned char)ch - 0x20;
    if (idx < 0 || idx >= 64) return;
    const uint8_t *glyph = s_font[idx];
    for (int row = 0; row < 8; row++)
        for (int col = 0; col < 8; col++)
            if (glyph[row] & (0x80u >> col))
                draw_fill_rect(r, x + col * scale, y + row * scale, scale, scale);
}

void draw_text(SDL_Renderer *r, const char *s, int x, int y, int scale) 
{
    for (; *s; s++, x += 8 * scale)
        draw_char(r, *s, x, y, scale);
}

int draw_center_x(const char *s, int scale) 
{
    return (SCR_WIDTH - (int)strlen(s) * 8 * scale) / 2;
}

void draw_text_centered(SDL_Renderer *r, const char *s, int y, int scale) 
{
    draw_text(r, s, draw_center_x(s, scale), y, scale);
}
